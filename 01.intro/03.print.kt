// La declaracion println permite imprimir mensajes en la pantalla
// La declaracion println automaticamente añade un salto de linea al final de la impresion
// La declaracion println puede recibir como parametro cualquier tipo de valor valido del lenguaje

fun main() {
    println("Hello World") // Hello World

    // La declaracion print permite imprimir mensajes en la pantalla, sin añadir un salto de linea al final de la impresion
    print(2 + 2) // 4
}
